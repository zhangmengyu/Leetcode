/**
 * @param {character[][]} board
 * @return {boolean}
 */
var isValidSudoku = function (board) {
  let rows = [{}, {}, {}, {}, {}, {}, {}, {}, {}],
    columns = [{}, {}, {}, {}, {}, {}, {}, {}, {}],
    boxes = [{}, {}, {}, {}, {}, {}, {}, {}, {}];
  let i, j;
  for (i = 0; i < board.length; i++) {
    for (j = 0; j < board.length; j++) {
      if (board[i][j] === '.') continue;
      if (rows[i][board[i][j]] !== undefined || columns[j][board[i][j]] !== undefined || boxes[(Math.floor(i / 3) * 3 + Math.floor(j / 3))][board[i][j]] !== undefined) return false;
      rows[i][board[i][j]] = 1;
      columns[j][board[i][j]] = 1;
      boxes[(Math.floor(i / 3) * 3 + Math.floor(j / 3))][board[i][j]] = 1;
    }
  }
  if (i === board.length && j === board.length) return true;
};
